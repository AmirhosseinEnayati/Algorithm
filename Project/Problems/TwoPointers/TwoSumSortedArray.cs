namespace Project.Problems.TwoPointers;

public class TwoSumSortedArray//167
{
    public int[] TwoSum2(int[] numbers, int target)
    {
        var left = 0;
        var right = numbers.Length - 1;

        while (left < right)
        {
            var sum = numbers[left] + numbers[right];

            if (sum == target)
                return [++left, ++right];
            if (sum > target)
                right--;
            else
                left++;
        }
        return [-1, -1];
    }
}